package Other;
public class coreandvalue {

  private int x;
  private int y;
  private int value;
  private int layer;
  public coreandvalue(int x , int y , int value , int layer){
    this.x = x;
    this.y = y;
    this.value = value;
    this.layer = layer ;
  }
  
  public int getX() {
    return x;
  }
  
  public int getY() {
    return y;
  }
  
  public int getValue() {
    return value;
  }
  
  public int getLayer() {
	  return layer;
  }
  
}